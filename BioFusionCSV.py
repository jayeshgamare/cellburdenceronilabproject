# Function to calculate the maximal PWM score
def calculate_max_pwm_score(seq, pwm):
    score = 0.0
    for i, nucleotide in enumerate(seq):
        if nucleotide == 'A':
            score += pwm['A'][i]
        elif nucleotide == 'C':
            score += pwm['C'][i]
        elif nucleotide == 'G':
            score += pwm['G'][i]
        elif nucleotide == 'T':
            score += pwm['T'][i]
    return score

