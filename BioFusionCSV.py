import os
import pandas as pd

def combine_csv_to_excel(csv_dir, excel_file):
    # Get a list of all CSV files in the directory
    csv_files = [file for file in os.listdir(csv_dir) if file.endswith('.csv')]

    # Create an empty list to store the individual dataframes
    dataframes = []

    # Read each CSV file and append its data to the dataframes list
    for csv_file in csv_files:
        csv_path = os.path.join(csv_dir, csv_file)
        df = pd.read_csv(csv_path)
        dataframes.append(df)

    # Concatenate all dataframes into a single dataframe
    combined_data = pd.concat(dataframes)

    # Split the first column on ";"
    split_data = combined_data.iloc[:, 0].str.split(";", expand=True)

    # Rename the split columns
    split_data.columns = [
        "Matrix",
        "Position",
        "Strand",
        "Binding Sequence",
        "Score",
        "Core Score",
        "Locus",
        "Distance to ATG",
        "Product",
    ]

    # Drop the original first column
    combined_data = combined_data.drop(combined_data.columns[0], axis=1)

    # Concatenate the split data with the remaining columns
    cleaned_data = pd.concat([split_data, combined_data], axis=1)

    # Write the cleaned data to a new Excel file
    cleaned_path = os.path.join(csv_dir, "Cleaned Data.xlsx")
    cleaned_data.to_excel(cleaned_path, index=False)

    print("Data combined and cleaned, saved to", cleaned_path)

